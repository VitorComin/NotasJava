package Trabalho;

public class Aluno {

    private String nome;
    private Float nota;

    public Aluno(String nome) {
    
        this.nome = nome;
    }

    public String getNome() {
    
        return nome;
    }

    public void setNome(String nome) {
    
        this.nome = nome;
    }

    public Float getNotas() {
    
        return notas;
    }

    public void setNota(Float notas) {
    
        this.notas = notas;
    }
}
